public class Compass{
	public static void compass(int degree){
	System.out.println("degree:"+degree);
	}
	public static void compass(char dirInitial){
	System.out.println("direction initials:"+dirInitial);
	}
	public static void compass(boolean useOfLocation){
	System.out.println("location used"+useOfLocation);
	}
	public static void compass(String direction){
	System.out.println("direction:"+direction);
	}
}